package com.py.model.baidu;

public class BaiduLocation {

	/**
	 * 经度
	 */
	private double lng;
	/**
	 * 纬度
	 */
	private double lat;

	public double getLng() {
		return lng;
	}

	public void setLng(double lng) {
		this.lng = lng;
	}

	public double getLat() {
		return lat;
	}

	public void setLat(double lat) {
		this.lat = lat;
	}
}